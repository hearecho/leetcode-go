package shudu

import (
	"fmt"
	"testing"
)

type param struct {
	board [][]byte
}

func Test_isValidSudoku(t *testing.T) {
	params := []param{
		{[][]byte{
			{'5', '3', '.', '.', '7', '.', '.', '.', '.'},
			{'3', '.', '.', '1', '9', '5', '.', '.', '.'},
			{'.', '9', '8', '.', '.', '.', '.', '6', '.'},
			{'8', '.', '.', '.', '6', '.', '.', '.', '3'},
			{'4', '.', '.', '8', '.', '3', '.', '.', '1'},
			{'7', '.', '.', '.', '2', '.', '.', '.', '6'},
			{'.', '6', '.', '.', '.', '.', '2', '8', '.'},
			{'.', '.', '.', '4', '1', '9', '.', '.', '5'},
			{'.', '.', '.', '.', '8', '.', '.', '7', '9'},
		}},
	}
	for _,p := range params {
		fmt.Printf("【input】:%v\t【output】:%v\n",p,isValidSudoku(p.board))
	}
}
